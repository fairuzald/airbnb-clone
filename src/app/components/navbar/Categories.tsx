const Categories = () => {
  // Get the search parameters and current pathname from the URL
  const params = useSearchParams();
  const pathname = usePathname();
  // Get the value of the "category" parameter from the search parameters
  const category = params?.get("category");

  // If the current pathname is not the root ("/") URL, return null and don't render anything
  if (pathname !== "/") {
    return null;
  }

  // Render a list of CategoryBox components, passing the label, icon, and selected status
  return (
    <div className="flex w-full items-center justify-center gap-5">
      {categories.map((item) => (
        <CategoryBox
          key={item.label}
          label={item.label}
          icon={item.icon}
          selected={category === item.label}
        />
      ))}
    </div>
  );
};

